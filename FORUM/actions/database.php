*PHP Data Objects est une extension définissant l'interface pour accéder à une base de données avec PHP. 
Elle est orientée objet, la classe s’appelant PDO. 
PDO constitue une couche d'abstraction qui intervient entre l'application PHP et un système de gestion de base de données tel que MySQL ou PostgreSQL.